class Enhancement:
    def __init__(self, name, effID, captionID, field3E70C175, roleType = M, param1 = [], param2 = [], skillIcon = 0, isArts = True, isSkill = True, isAccessory = True):
        self.name = name
        self.effID = effID
        self.captionID = captionID
        self.field3E70C175 = field3E70C175
        self.param1 = param1
        self.param2 = param2
        self.roleType = roleType
        self.skillIcon = skillIcon
        if isArts:
            ArtsEnhancementList.append(self)
        if isAccessory:
            AccessoryEnhancementList.append(self)
        if isSkill:
            SkillEnhancementList.append(self)
        EnhancementsList.append(self)
    
    def CreateEffect(self, BTL_EnhanceData, param1 = None, param2 = None):
        '''Returns the new Enhancements ID'''
        newID = len(BTL_EnhanceData["rows"])+1
        newEffect = {
        "$id": newID,
        "ID": "",
        "EnhanceEffect": self.effID,
        "Param1": random.randrange(param1[0], param1[1]),
        "Param2": random.randrange(param2[0], param2[1]),
        "Caption": self.captionID,
        "DebugName": "",
        "AcceSetCheck": self.effID,
        "<3E70C175>": self.field3E70C175
        }
        BTL_EnhanceData["rows"].append(newEffect)  
        return newID    

